package Activities;

public class loop_while {

	public static void main(String[] args) {
        int x=1;
        //exit when x becomes greater than 4
        
        while (x<=4) {
        	
        	System.out.println("Value of x:" +x);
        	
        	//increment the value of x for next iteration
        	
        	x++;
        	
        }

	}

}
